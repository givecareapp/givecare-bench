#!/usr/bin/env python3
"""LoRA fine-tune Qwen3.5-4B on caregiving transcripts using MLX.

This is the file the autoresearch agent modifies between experiments.
It controls: LoRA rank, learning rate, batch size, data selection.

Usage:
    python autoresearch/train/train.py              # Train with defaults
    python autoresearch/train/train.py --iters 200  # Quick test
"""

import subprocess
import sys
import time
from pathlib import Path

# ── Config (agent modifies these) ──────────────────────────────

MODEL = "mlx-community/Qwen3.5-4B-MLX-4bit"
DATA_DIR = "autoresearch/train/data"
ADAPTER_DIR = "autoresearch/train/adapters"
FUSED_DIR = "autoresearch/train/fused-model"

LORA_RANK = 8
LORA_LAYERS = 16
LEARNING_RATE = 1e-5
BATCH_SIZE = 2
ITERS = 500
GRAD_ACCUM = 4  # effective batch = BATCH_SIZE * GRAD_ACCUM = 8

# ── Training ───────────────────────────────────────────────────


def write_config():
    """Write LoRA config YAML."""
    config_path = Path("autoresearch/train/lora_config.yaml")
    config_path.parent.mkdir(parents=True, exist_ok=True)
    config_path.write_text(f"""\
# Auto-generated by train.py — agent may modify
model: "{MODEL}"
data: "{DATA_DIR}"
adapter_path: "{ADAPTER_DIR}"
lora_layers: {LORA_LAYERS}
lora_parameters:
  rank: {LORA_RANK}
  alpha: {LORA_RANK * 2}
  dropout: 0.05
  scale: {20.0 / LORA_RANK}
learning_rate: {LEARNING_RATE}
batch_size: {BATCH_SIZE}
iters: {ITERS}
grad_checkpoint: true
val_batches: 5
steps_per_report: 10
steps_per_eval: 50
save_every: 100
""")
    return config_path


def train():
    """Run LoRA training via mlx_lm."""
    config_path = write_config()

    print(f"Model:    {MODEL}")
    print(f"LoRA:     rank={LORA_RANK}, layers={LORA_LAYERS}")
    print(f"LR:       {LEARNING_RATE}")
    print(f"Batch:    {BATCH_SIZE} x {GRAD_ACCUM} grad_accum = {BATCH_SIZE * GRAD_ACCUM}")
    print(f"Iters:    {ITERS}")
    print(f"Config:   {config_path}")
    print()

    start = time.time()
    result = subprocess.run(
        [sys.executable, "-m", "mlx_lm.lora", "--config", str(config_path)],
        capture_output=False,
    )
    elapsed = time.time() - start

    print(f"\nTraining completed in {elapsed:.0f}s (exit code: {result.returncode})")
    return result.returncode


def fuse():
    """Fuse LoRA adapters into base model."""
    print(f"\nFusing adapters into {FUSED_DIR}...")
    result = subprocess.run(
        [
            sys.executable, "-m", "mlx_lm.fuse",
            "--model", MODEL,
            "--adapter-path", ADAPTER_DIR,
            "--save-path", FUSED_DIR,
        ],
        capture_output=False,
    )
    print(f"Fuse completed (exit code: {result.returncode})")
    return result.returncode


def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--iters", type=int, default=None)
    parser.add_argument("--fuse", action="store_true", help="Fuse after training")
    args = parser.parse_args()

    if args.iters:
        global ITERS
        ITERS = args.iters

    # Check data exists
    data_dir = Path(DATA_DIR)
    if not (data_dir / "train.jsonl").exists():
        print("No training data. Run: python autoresearch/train/prepare.py")
        sys.exit(1)

    rc = train()
    if rc != 0:
        sys.exit(rc)

    if args.fuse:
        fuse()


if __name__ == "__main__":
    main()
